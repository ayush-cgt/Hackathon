public class Client {

    String id;
    String name;
    String company;
    String email;
    String country;
    String city;
    String zip;
    String address;
    String phone;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String txt) {
        this.name = txt;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String txt) {
        this.company = txt;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String txt) {
        this.email = txt;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String txt) {
        this.country = txt;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String txt) {
        this.city = txt;
    }

    public String getZip() {
        return zip;
    }

    public void setZip(String txt) {
        this.zip = txt;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String txt) {
        this.address = txt;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String txt) {
        this.phone = txt;
    }
}
